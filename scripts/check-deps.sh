#!/bin/bash
#
# 依赖检查脚本 - 检查 CarLife 项目的 npm 依赖更新情况
#

echo "============================================================"
echo "📅 $(date '+%Y-%m-%d %H:%M:%S')"
echo "🔍 CarLife 依赖检查"
echo "============================================================"

# 检查过时的依赖
echo ""
echo "============================================================"
echo "📦 检查过时的 npm 依赖包"
echo "============================================================"

if command -v npm &> /dev/null; then
    npm outdated 2>/dev/null || echo "未发现过时的依赖或 npm outdated 不可用"
else
    echo "❌ npm 未安装"
    exit 1
fi

# 检查安全漏洞
echo ""
echo "============================================================"
echo "🔒 检查安全漏洞"
echo "============================================================"

if npm audit --audit-level=moderate 2>&1; then
    echo "✅ 未发现严重/高危安全漏洞"
fi

# 生成总结
echo ""
echo "============================================================"
echo "📊 检查总结"
echo "============================================================"

echo "✅ 依赖检查完成"
echo ""
echo "💡 建议:"
echo "   1. 查看过时的依赖包详情"
echo "   2. 评估升级的必要性和风险"
echo "   3. 定期运行 npm audit 检查安全漏洞"
